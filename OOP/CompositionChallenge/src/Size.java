public class Size {
    private int widht;
    private int length;
    private double height;

    public Size(int widht, int length, double height) {
        this.widht = widht;
        this.length = length;
        this.height = height;
    }

    public int getWidht() {
        return widht;
    }

    public int getLength() {
        return length;
    }

    public double getHeight() {
        return height;
    }
}
